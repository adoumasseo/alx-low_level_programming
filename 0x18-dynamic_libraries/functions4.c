/**
 * _strpbrk - Entry point
 * @s: input
 * @accept: input
 * Return: Always 0 (Success)
 */
char *_strpbrk(char *s, char *accept)
{
		int k;

		while (*s)
					{
			for (k = 0; accept[k]; k++)
			{
			if (*s == accept[k])
			return (s);
			}
		s++;
		}

	return ('\0');
}

/**
 * _strstr - Entry point
 * @haystack: input
 * @needle: input
 * Return: Returns 0 (Success)
 */
char *_strstr(char *haystack, char *needle)
{
	for (; *haystack != '\0'; haystack++)
	{
		char *l = haystack;
		char *p = needle;

		while (*l == *p && *p != '\0')
		{
			l++;
			p++;
		}

		if (*p == '\0')
			return (haystack);
	}

	return (0);
}

/**
 *_abs - computes the absolute value of an integer
 * @n: the int to check
 * Return: the absolute value of int
*/
int _abs(int n)
{
	if (n >= 0)
	{
		return (n);
	}
	return (-n);
}
